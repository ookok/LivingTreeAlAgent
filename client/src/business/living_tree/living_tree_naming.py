# ==================== 辅助函数 ====================

def get_living_name(tech_term: str) -> str:
    """
    获取技术术语的生命之树命名

    Args:
        tech_term: 技术术语

    Returns:
        str: 生命之树命名
    """
    # 按类别查找
    all_mappings = [
        TRUNK_NAMING,
        HEARTWOOD_NAMING,
        ROOT_NETWORK_NAMING,
        SAP_CHANNEL_NAMING,
        FOREST_BAZAAR_NAMING,
        ASSEMBLY_GARDEN_NAMING,
        CANOPY_VISTA_NAMING,
    ]

    for mapping in all_mappings:
        if tech_term in mapping:
            return mapping[tech_term]

    return tech_term  # 未找到则返回原名


def get_narrative(key: str, **kwargs) -> str:
    """
    获取叙事台词

    Args:
        key: 台词键
        **kwargs: 格式化参数

    Returns:
        str: 格式化后的台词
    """
    if key not in NARRATIVE_LINES:
        return key

    template = NARRATIVE_LINES[key]
    if kwargs:
        return template.format(**kwargs)
    return template


def format_tech_with_living(tech_term: str) -> str:
    """
    格式化：原术语 → 新命名

    Args:
        tech_term: 技术术语

    Returns:
        str: 格式化后的文本
    """
    living_name = get_living_name(tech_term)
    if living_name == tech_term:
        return tech_term
    return f"{tech_term} ({living_name})"
